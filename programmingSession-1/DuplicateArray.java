package ProgrammingSession;

public class DuplicateArray 
{
	public static void main(String[] args) 
	{
		int a[]= {10,20,30,40,11};
		for(int b:a)
		{
			System.out.println(b);
	}
     System.out.println("_____________________");
     int c[]=a;
     for(int d:c)
     {
    	 System.out.println(d);
     }
}
}