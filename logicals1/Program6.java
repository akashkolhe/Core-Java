package logicals1;

public class Program6 {
	
	public static void main(String[] args) {
		int a = 10;
		a+=10; // a= a+10 = 10+10=20
		System.out.println(a);
		
		a-=5; // a =a-5 = 20-5=15
		System.out.println(a);
		
		a*=2; // a= a*2 = 15 *2 = 30
		System.out.println(a);
		
		a/=10; // a=a/10 = 30 /10=3
		System.out.println(a);
	}

}
